package Session5_LoopingConstruct;

/**
 *
 * @author June
 */
public class for_loop {

    public static void main(String[] args) {
        System.out.println("Hiển thị các số từ 1 đến 10(Sử dụng vòng lặp for)");
        for (int i = 1; i <= 10; i++) {
            System.out.println(i + " ");
        }
        System.out.println("");
    }
}
